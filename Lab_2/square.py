import math

# Get user input for three numbers
number1 = float(input("Enter the first number: "))
number2 = float(input("Enter the second number: "))
number3 = float(input("Enter the third number: "))

# Compute the square roots
sqrt1 = math.sqrt(number1)
sqrt2 = math.sqrt(number2)
sqrt3 = math.sqrt(number3)

# Compute the sum of the square roots
sum_of_sqrts = sqrt1 + sqrt2 + sqrt3

# Print the result
print("The sum of the square roots is:", sum_of_sqrts)

