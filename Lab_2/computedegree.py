# Compute a)sin of 60 degree b)cos of pi c)sin(0.8660254037844386) d)tan of 90 degree
import math

# Computing the values
a = math.sin(math.radians(60))  # sin of 60 degrees
b = math.cos(math.pi)  # cos of pi
c = math.sin(0.8660254037844386)  # sin of 0.8660254037844386
d = math.tan(math.radians(90))  # tan of 90 degrees

print(f"a) sin(60 degrees) = {a}")
print(f"b) cos(pi) = {b}")
print(f"c) sin(0.8660254037844386) = {c}")
print(f"d) tan(90 degrees) = {d}")
